"""
Demostrates different types of for loops
@abhsihek singh
"""

list = ['A', 'B', 'C', 'D', 'E']

print('\nfor in loop:')
for c in list:
    print(c)

print('\nrange(n):')
for i in range(5):
    print(i)

print('\nrange(m,n):')
for i in range(3, 10):
    print(i)

print('\nrange(m,n,o):')
for i in range(3, 15, 2):
    print(i)
